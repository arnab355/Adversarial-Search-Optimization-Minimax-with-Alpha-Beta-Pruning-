def cal_2(p, g):
    pool_without_s = [n for n in p if n != 'S']
    score_normal, gene_normal = minimax("", pool_without_s, g, len(pool_without_s), -math.inf, math.inf, True)

    if 'S' in p:
        score_special, gene_special = minimax("", p, g, len(p), -math.inf, math.inf, True, special=True)
        decision = "YES" if score_special > score_normal else "NO"
        print("\nTask II Output:")
        print(decision)
        print("With special nucleotide")
        print(f"Best gene sequence generated: {gene_special}")
        print(f"Utility score: {round(score_special, 2)}")
    else:
        print("\nTask II Output:")
        print("NO")
        print("No special nucleotide 'S' in pool.")

